// Examples of relational operators
// Greater than >
console.log(`is (5>3)? ` + (5>3))
console.log(`is (2>3)? ` + (2>3))
// Greater than or Equal to >=
console.log(`is (5>=5)? ` + (5>=5))
console.log(`is (5>=6)? ` + (5>=6))
// Less than <
console.log(`is (5<6)? ` + (5<6))
console.log(`is (6<5)? ` + (6<5))
// Less than or Equal to <=
console.log(`is (5<=5)? ` + (5<=5))
console.log(`is (6<=5)? ` + (6<=5))
