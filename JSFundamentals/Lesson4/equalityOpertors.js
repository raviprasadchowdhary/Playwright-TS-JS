// Equality Operators
// Equal to ==
console.log(`is (1==1)? ` + ((1==1)))
console.log(`is (1=='1')? ` + ((1=='1')))
console.log(`is (1==2)? ` + ((1==2)))
// Not Equal to !=
console.log(`is (1!=1)? ` + ((1!=1)))
console.log(`is (1!=2)? ` + ((1!=2)))
// Strict Equal to ===
console.log(`is (1===1)? ` + ((1===1)))
console.log(`is (1==='1')? ` + ((1==='1')))
// Strict Not Equal to !==
console.log(`is (1!=='1')? ` + ((1!=='1')))
console.log(`is (1!==1)? ` + ((1!==1)))